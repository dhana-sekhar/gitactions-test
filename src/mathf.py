class Mathfunc:

    def adding(a: int,b: int) -> int:
        """This is an addition math function

        Returns:
            _type_: returns sum of given 2 parametes
        """

        return a+b

    def subt(a: int,b: int) -> int: 
        """This is an Sub math function

        Args:
            a (int): first value
            b (int): second value

        Returns:
            _type_: reuturns sub of given 2 parameters 
        """
        return a-b